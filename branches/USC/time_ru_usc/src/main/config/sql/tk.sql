CREATE TRIGGER TK_KIOSK_T_LOG
before insert or update or delete
on TK_KIOSK_T for each row
declare
    sesn_id  tk_user_sesn_tmp_t.tk_user_sesn_id%type;
    emp_id   tk_user_sesn_tmp_t.pplsft_emp_id%type;
    ip_addr  tk_user_sesn_tmp_t.tk_usr_ip_addr%type;
    kiosk_id     tk_user_sesn_tmp_t.kiosk_id%type;
    counter     pls_integer;
    update_cd   varchar2(1);
    msg         varchar2(2000);
begin
    if deleting then
        sesn_id := :old.TK_LOG_SESN_ID;
    else
        sesn_id := :new.TK_LOG_SESN_ID;
    end if;

    if sesn_id is not null then
        if (sesn_id = 'SYSTEM') then
            emp_id := 'SYSTEM';
            ip_addr := 'SYSTEM';
            kiosk_id := 'SYSTEM';
        else
            select count(*) into counter
            from tk.tk_user_sesn_tmp_t
            where tk_user_sesn_id = sesn_id;

            if (counter > 0) then
                select a.pplsft_emp_id, a.tk_usr_ip_addr, a.kiosk_id
                into emp_id, ip_addr, kiosk_id
                from tk.tk_user_sesn_tmp_t a
                where tk_user_sesn_id = sesn_id;
            else  --session id not present in session table
                emp_id := 'BAD SESN ID';
                ip_addr := 'BAD SESN ID';
                kiosk_id := 'BAD SESN ID';
            end if;
        end if;
    else   --session id null
        emp_id := 'NUL SESN ID';
        ip_addr := 'NUL SESN ID';
        kiosk_id := 'NUL SESN ID';
    end if;

    if inserting then
        update_cd := 'I';
    elsif updating then
        update_cd := 'U';
    elsif deleting then
        update_cd := 'D';
    end if ;

    if deleting then
        insert into TK_KIOSK_HT(
        KIOSK_ID, KIOSK_DESC, PPLSFT_DEPT_ID, TK_USR_IP_ADDR,
        tk_log_emp_id, tk_log_ip_addr, tk_log_kiosk_id, tk_log_ts, tk_log_updt_cd
        ) values (          --use :old
        :old.KIOSK_ID, :old.KIOSK_DESC, :old.PPLSFT_DEPT_ID, :old.TK_USR_IP_ADDR,
        emp_id, ip_addr, kiosk_id, sysdate, update_cd);
    else
        insert into TK_KIOSK_HT(
        KIOSK_ID, KIOSK_DESC, PPLSFT_DEPT_ID, TK_USR_IP_ADDR,
        tk_log_emp_id, tk_log_ip_addr, tk_log_kiosk_id, tk_log_ts, tk_log_updt_cd
        ) values (          --use :new
        :new.KIOSK_ID, :new.KIOSK_DESC, :new.PPLSFT_DEPT_ID, :new.TK_USR_IP_ADDR,
        emp_id, ip_addr, kiosk_id, sysdate, update_cd);
    end if;

exception
    when others then
        msg := 'trigger TK_KIOSK_T_LOG: error '||SQLERRM;
        log_application_error(msg);
        raise_application_error(-20001, msg);
end;
GO
